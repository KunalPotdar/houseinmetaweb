// Language state
let currentLang = localStorage.getItem('houseInMetaLang') || 'en';

// Toggle language function
function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'fr' : 'en';
  localStorage.setItem('houseInMetaLang', currentLang);
  updatePageLanguage();
}

// Update all translatable elements
function updatePageLanguage() {
  // Update HTML lang attribute
  document.documentElement.lang = currentLang;
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = translations[currentLang][key];
    
    if (translation) {
      // Handle different element types
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translation;
      } else {
        element.textContent = translation;
      }
    }
  });
  
  // Update language toggle button
  updateLanguageButton();
}

// Update language button appearance
function updateLanguageButton() {
  const langBtn = document.getElementById('langToggle');
  if (!langBtn) return;
  
  if (currentLang === 'fr') {
    langBtn.innerHTML = '<span class="lang-flag">ðŸ‡¬ðŸ‡§</span> <span class="lang-code">EN</span>';
    langBtn.setAttribute('aria-label', 'Switch to English');
  } else {
    langBtn.innerHTML = '<span class="lang-flag">ðŸ‡«ðŸ‡·</span> <span class="lang-code">FR</span>';
    langBtn.setAttribute('aria-label', 'Passer au franÃ§ais');
  }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  updatePageLanguage();
});

